#include<unistd.h>

void	ft_is_negative(int number)
{
	char	n;
	char	p;

	n = 'N';
	p = 'P';
	if (number < 0)
	{
		write (1, &n, 1);
	}
	else
	{
		write(1, &p, 1);
	}
}
